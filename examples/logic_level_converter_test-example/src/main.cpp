
#include "Arduino.h"


//#include <Narcoleptic.h> //low-power sleep


//////////////////////////////////////////////////////////////////////////
// Configuration
//////////////////////////////////////////////////////////////////////////



// Pin Assignment
const uint8_t pin0 = 10;
const uint8_t pin1 = 11;
const uint8_t pin2 = 12;
const uint8_t pin3 = 13;



////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////
//// Initial setup
////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////
void setup(void)
{
    Serial.begin(9600);
    Serial.println("Debug: Startup");
  pinMode(pin0, OUTPUT);
  pinMode(pin1, OUTPUT);
  pinMode(pin2, OUTPUT);
  pinMode(pin3, OUTPUT);

}


/////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////
//// LOOP forever
/////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////
void loop(void){
  delay(1000);
  digitalWrite(pin0,HIGH);
  Serial.print("one.");
  delay(1000);
  digitalWrite(pin1,HIGH);
  Serial.print("two.");
  delay(1000);
  digitalWrite(pin2,HIGH);
  Serial.print("three.");
  delay(1000);
  digitalWrite(pin3,HIGH);
  Serial.print("four.");
  delay(1000);
  digitalWrite(pin0,LOW);
  digitalWrite(pin1,LOW);
  digitalWrite(pin2,LOW);
  digitalWrite(pin3,LOW);
  Serial.print("XOXOXO.");
}
